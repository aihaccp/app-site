    <h3><b>Higiene</b></h3>
    <br>
    <h5>Introdução:</h5>

    <p>Neste ponto 2 do caderno serão descritos os procedimentos referentes à Higiene. Ponto fundamental
    para evitar contaminações cruzadas de alimentos e superfícies.
    </p>


    <p>Os procedimentos descritos neste ponto são:
    </p>
    <ul>
        <li id="point">Higiene Pessoal;</li>
        <li id="point">Eficiência na Limpeza</li>
        <li id="point">Plano de Higiene</li>
    </ul>
    <br>
    <div class="container mt-4">
        <h3> <strong> <u>Definições:</u> </strong> </h3>
        <div>
            <p>
            <strong>Detergente:</strong>
            Funcionários que apresentem sintomas de intoxicação alimentar - Não deverão
                trabalhar diretamente com gêneros alimentícios, podendo eventualmente serem transferidos para uma
                área que não possa haver o contacto direto com alimentos;</p>
            <p>
            <strong>Diluições:</strong>
            Ato ou efeito de diluir de uma concentração maior para uma mais pequena. Essa diluição é
normalmente feita com água. As diluições são necessárias porque normalmente os produtos de
limpeza vêm concentrados e há a necessidade de juntar água, para promover a chamada diluição. As
diluições deverão ser feitas conforme instruções do fabricante. As diluições são importantes serem
seguidas porque uma contaminação ou uma limpeza adequada depende dessa diluição;</p>
<p>
    <strong>Desinfetante: </strong>
        Produto químico usado para a eliminação das bactérias. A aplicação do desinfetante
        depende de uma limpeza eficaz antes da desinfeção;</p>
        <p>
            <strong>Tempo de Atuação:</strong>
            : é o período de tempo que o produto químico necessita de estar em contacto, com
            a superfície a higienizar, para a higienização ser o mais eficaz possível. Estas instruções vêm
            mencionadas nas respetivas fichas técnicas;</p>
        <p>
            <strong>Detergente Desinfetante:</strong>
            Produto químico que tem a ação de limpeza e desinfeção, num único
            produto. Tal como os restantes produtos químicos, este também deverá ser usado conforme as
            instruções do fornecedor.
            </p>
        </div>
    </div>





