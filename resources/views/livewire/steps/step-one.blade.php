    <h4 style="font-size:1.2rem;"><b>1.3. Instalações e equipamentos</b></h4>
    <br>
    <p>Tanto as instalações como os equipamentos, se não forem devidamente tratados, corre-se o risco dos mesmos serem o
        vetor de contaminações cruzadas. Devido a isso, é de extrema importância, realizar manutenções periódicas às
        instalações e equipamentos.
    </p>
    <table>
        <tr>
            <th>Ponto de segurança</th>
            <th>Porquê</th>
            <th>Como controlar os pontos de segurança?</th>
        </tr>
        <!-- Continuação das linhas da tabela para Equipamentos / Utensílios -->
        <tr>
            <td>a) Equipamentos e utensílios que se encontram partidos, estalados ou danificados devem ser substituídos
                ou arranjados.</td>
            <td>Nas zonas danificadas é o local propício à acumulação e desenvolvimento de microrganismos. As
                superfícies danificadas são normalmente de difícil limpeza e desinfecção, não permitindo desta forma a
                eliminação dos microrganismos.</td>
            <td>Aquando da visita às instalações ou durante a hora de trabalho, fazer verificações das condições de
                conservação e integridade dos equipamentos e utensílios.</td>
        </tr>
        <tr>
            <td>b) Se forem usadas tábuas de corte, as mesmas deverão ser substituídas sempre que se apresentem gastas
                ou com golpes profundos.</td>
            <td>O facto das superfícies não se apresentarem lisas, faz com que os microrganismos se possam alojar em
                locais que o processo de limpeza e desinfecção não conseguem alcançar.</td>
            <td>Verificações periódicas aos pontos de segurança. Verificar também a existência gráfica ou tabela de
                cores correspondentes à cada área de trabalho.</td>
        </tr>
        <tr>
            <td>c) Deverão ser usadas tábuas de corte com cores diferentes para cada um dos tipos de produto que se está
                a manipular. O mesmo se passa com as facas de corte.</td>
            <td>Usando um código de cores consegue-se garantir o uso de uma cor específica para cada tipo de produto
                (ex.: carne e peixe).</td>
            <td>Verificar se os equipamentos estão a ser usados convenientemente (conformidade com o uso correto).</td>
        </tr>
        <tr>
            <td>d) Os equipamentos que são usados para a manutenção de temperaturas, de confeção e ou conservação têm de
                ser vistoriados com frequência a fim de assegurar o seu correto funcionamento.</td>
            <td>Se os equipamentos que são responsáveis pela conservação dos alimentos, não funcionarem corretamente,
                vamos por em causa a segurança alimentar.</td>
            <td>Verificar mensalmente o estado e o funcionamento correto (confirmar com termómetro de precisão).</td>
        </tr>
        <tr>
            <td style=" background-color: #f2f2f2;text-align: center;" colspan="3"><b>Instalações</b>
            </td>
        </tr>
        <tr>
            <td>e) A estrutura das instalações deve apresentar superfícies lisas, impermeáveis, de material
                imputrescível. Assim, sempre que sejam detetados danos nas instalações, tais como – humidades, bolores,
                buracos ou superfícies partidos, tinta lascada, entre outros - deverá proceder-se de imediato à sua
                reparação.</td>
            <td>Uma superfície danificada não permite uma limpeza e desinfecção adequada, para além disso há a
                possibilidade de atrair pragas.</td>
            <td>Verificações periódicas com vista à verificação dos pontos de segurança.</td>
        </tr>
        <tr>
            <td>f) Os filtros e ventoinhas que sejam usadas nas instalações deveram ser verificadas, a fim de garantir
                que as mesmas se encontram a funcionar.</td>
            <td>As ventoinhas e eventualmente os filtros servem para manter um ambiente adequado no local de trabalho.
                Se os mesmos não estiverem a funcionar convenientemente, poder-se-á criar condensações, acumulação de
                gordura e maus cheiros.</td>
            <td>Verificações periódicas com vista à verificação dos pontos de segurança.</td>
        </tr>
