    <h4 style="font-size:1.2rem;"><b>1.1. Manipuladores</b></h4>
    <br>
    <p>Os manipuladores são um veículo de contaminações cruzadas e é vital serem seguidas as regras abaixo descritas:
    </p>
    <table>
        <tr>
            <th>Ponto de segurança</th>
            <th>Porquê</th>
            <th>Como controlar os perigos?</th>
        </tr>
        <tr>
            <td>
                <strong>a) Todos os manipuladores devem lavar as mãos cuidadosamente</strong> antes de preparar ou
                manipular os alimentos. (Ver método de "lavagem das mãos" na Seção Limpeza.)
                <hr>
                <strong>b) Todos os manipuladores devem usar roupas limpas</strong> quando manipulam alimentos.
                Idealmente, deveriam trocar a roupa da rua, por uma farda de trabalho limpa, antes de começar a
                trabalhar. A farda de trabalho não deverá ser usada fora das áreas de preparação ou manipulação de
                alimentos.
                <hr>
                <strong>c) A farda de trabalho deve ter mangas compridas</strong> e de cor clara (para evidenciar a
                sujidade), sem bolsos externos e molas em vez de botões. É também uma boa ideia usar um avental limpo ou
                avental descartável sobre a roupa de trabalho.
            </td>
            <td>
                A lavagem das mãos é uma das melhores formas de evitar que as bactérias nocivas se espalhem por
                superfícies e alimentos.
                <hr>
                A roupa pode ser o veículo de portador de bactérias e sujidades para o interior das áreas de preparação
                de alimentos. Vestindo a farda de trabalho é um fator essencial para evitar isso.
                <hr>
                Evita-se desta forma que a pele a partir dos alimentos tocar e ajuda a parar de cabelos, fibras e o
                conteúdo dos bolsos (que pode levar bactérias) entrar em alimentos.
            </td>
            <td>
                Verificar no local, se os funcionários estão devidamente informados, de quando devem ser lavadas as
                mãos.
                <hr>
                Verificar no local, se os funcionários estão a usar uma farda de trabalho limpa.

                Intervir na hora, caso seja detetada uma anomalia aos pontos de segurança.
            </td>

        </tr>
        <tr>
            <td style=" background-color: #f2f2f2;text-align: center;" colspan="3"><b>Aptidão para o trabalho</b></td>
        </tr>
        <tr>
            <td>
                <strong>d) Todos os operários devem encontrar-se "apto para o trabalho"</strong> em todos os momentos.
                Isto significa que não devem manipular gêneros alimentícios se sofrerem de alguma doença que possa ser
                transmitida aos alimentos e por conseguinte por em causa a segurança alimentar.
                <hr>
                <strong>e) Qualquer membro do pessoal que apresente sintomas</strong>, como diarreia e/ou vómito deverá
                comunicar o facto ao seu superior imediato e quer ficar em casa ou ir para casa imediatamente.
                <hr>
                <strong>f) Os funcionários devem informar</strong> o seu superior hierárquico se apresentarem quaisquer
                cortes ou feridas. Os cortes e feridas devem ser desinfetadas e completamente cobertas com pensos
                coloridos e estanques. Se os cortes ou feridas forem nas mãos, deverão também calçar umas luvas.
            </td>
            <td>
                As pessoas que não são "aptos para o trabalho" poderiam espalhar bactérias ou vírus prejudiciais aos
                alimentos.
                <hr>
                As pessoas que sofrem desses sintomas muitas vezes são portadoras de bactérias nocivas e pode
                espalhá-las pelos alimentos ou equipamentos que tocam.
                <hr>
                Cortes e feridas podem transportar bactérias nocivas, como tal é necessário cobrir essas áreas de modo a
                impedir a proliferação das bactérias para os alimentos.
            </td>
            <td>
                Formar os operadores dos pontos de segurança.
                Melhorar a supervisão dos funcionários.

            </td>
        </tr>

