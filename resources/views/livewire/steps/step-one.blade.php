    <h4 style="font-size:1.2rem;"><b>1.2. Pragas</b></h4>
    <br>
    <p>O controlo das pragas é um requisito obrigatório, evitando que juntamente com as eventuais pragas, venham
        associadas bactérias, vírus e doenças, para o interior das instalações. Assim deveremos ter em atenção os
        seguintes procedimentos:
    </p>

    <table>
        <tr>
            <th>Ponto de segurança</th>
            <th>Porquê</th>
            <th>Como controlar os pontos de segurança?</th>
        </tr>
        <tr>
            <td>a) Verificar com regularidade vestígios de pragas, tais como:
                <ul>
                    <li id="point">Baratas – ovos, vezes e a própria barata;</li>
                    <li id="point">Aves – penas, fezes, ninhos, chilrear e as próprias aves;</li>
                    <li id="point">Roedores – pegadas, fezes, urina, superfícies roídas;</li>
                    <li id="point">Formigas – Pequenos montes de areia ou terra, asas de formigas voadoras, próprias
                        formigas;</li>
                    <li id="point">Insetos voadoras – asas, insetos mortos, larvas.</li>
                </ul>
            </td>
            <td>As pragas são portadoras de doenças, vírus e bactérias perigosas.</td>
            <td> Todos os funcionários estão instruídos para verificar a existência de indícios de pragas,</td>
        </tr>
        <tr>
            <td>b) Verificar com regularidade os relatórios deixado pela equipe de controle de pragas</td>
            <td>A verificação dos relatórios indica-nos se houve presença de pragas, podendo desta forma haver uma
                intervenção para corrigir essa ocorrência</td>
            <td>Sempre que há visitas de rotina da equipe de controle de pragas, os relatórios deverão ser verificados e
                comparados com os anteriores.</td>
        </tr>
        <tr>
            <td>c) Verificar sempre a receção da matéria-prima / produtos, a existência de evidências de pragas, tais
                como:
                <ul>
                    <li id="point">Caixas roídas, fezes de ratos, baratas, ratos.</li>
                </ul>
                Em caso de presença de pragas, a mercadoria deverá ser rejeitada. Se possível a caixa de cartão que
                envolveu a mercadoria deverá ser retirada e o exterior das instalações deverá ser uma zona limpa,
                desprovida de mato e acumulado de objetos obsoletos.
            </td>
            <td>Uma das forma de entrada de pragas, no interior das instalações é proveniente das embalagens que
                acondicionam as matérias-primas / produtos. Daí ter que seguir o descrito no ponto de segurança</td>
            <td>Quando da receção das mercadorias, as mesmas deverão sempre ser avaliadas e verificadas possíveis
                evidências de existências de pragas</td>
        </tr>
        <!-- Continuação das linhas da tabela -->
        <tr>
            <td>d) O exterior das instalações deverá ser uma zona limpa, desprovida de mato e amontoados de objetos
                obsoletos.</td>
            <td>Uma zona exterior que não se apresente limpa, torna-se um meio de atração de pragas.</td>
            <td>Verificar trimestralmente a área exterior.</td>
        </tr>
        <tr>
            <td>e) Os caixotes do lixo deverão encontrar-se em boas condições de conservação e limpeza e sempre
                fechados.</td>
            <td>O caixote do lixo é um excelente local para as pragas se alojarem.</td>
            <td>Verificar o estado de higiene dos recipientes do lixo. Os mesmos deverão ser limpos diariamente.</td>
        </tr>
        <tr>
            <td>f) O Armazém deverá estar organizado de forma a se poder ter acesso aos iscos. Os iscos não devem ser
                mexidos nem retirados dos locais respetivos.</td>
            <td>Se a mercadoria se encontrar encostada às paredes ou diretamente assente no pavimento, para além de
                dificultar a limpeza, não permite um acesso conveniente ao técnico de controle de pragas.</td>
            <td>As prateleiras encontram-se encostadas à parede? A mercadoria encontra-se em prateleiras e/ou estrados?
            </td>
        </tr>
