    <h3><b>Contaminação Cruzada</b></h3>
    <br>
    <h5>Introdução:</h5>

    <p>A contaminação cruzada é uma das causas mais comuns de intoxicação alimentar. A mesma sucede quando um
        alimento é contaminado por bactérias nocivas, provenientes de outros alimento, superfícies, mãos ou
        equipamentos.</p>
    <p> Estas bactérias nocivas muitas vezes vêm de matérias, tais como a carne / aves, peixes, ovos e vegetais não
        lavados. Por isso é de estrema importância seguir os procedimentos corretos para evitar a contaminação
        cruzada dos géneros alimentícios.</p>
    <p> Para minimizar ou evitar as contaminações cruzadas, serão descritos, seguidamente, os procedimentos a serem
        seguidos pelos trabalhadores da organização.
    </p>
    <br>
    <p>Procedimentos descritos:
    </p>
    <ul>
        <li id="point">Manipuladores</li>
        <li id="point">Pragas</li>
        <li id="point">Instalações e Equipamentos</li>
        <li id="point">Panos</li>
        <li id="point">Separação de Alimentos</li>
        <li id="point">Contaminação Física e Química</li>
    </ul>
